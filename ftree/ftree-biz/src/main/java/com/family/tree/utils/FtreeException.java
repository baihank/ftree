package com.family.tree.utils;

public class FtreeException extends RuntimeException{
	private static final long serialVersionUID = 7370563156915984691L;

	public FtreeException() {
		super();
	}

	public FtreeException(String message, Throwable cause) {
		super(message, cause);
	}

	public FtreeException(String message) {
		super(message);
	}

	public FtreeException(Throwable cause) {
		super(cause);
	}
	

}
